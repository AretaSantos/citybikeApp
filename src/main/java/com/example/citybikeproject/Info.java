package com.example.citybikeproject;

public class Info {
	
	String stationName;
	String stationAddress;
	int startedJourney;
	int endedJourney;
	int averageDistanceStart;
	int averageDistanceEnding;
	public String getStationName() {
		return stationName;
	}
	public void setStationName(String stationName) {
		this.stationName = stationName;
	}
	public String getStationAddress() {
		return stationAddress;
	}
	public void setStationAddress(String stationAddress) {
		this.stationAddress = stationAddress;
	}
	public int getStartedJourney() {
		return startedJourney;
	}
	public void setStartedJourney(int startedJourney) {
		this.startedJourney = startedJourney;
	}
	public int getEndedJourney() {
		return endedJourney;
	}
	public void setEndedJourney(int endedJourney) {
		this.endedJourney = endedJourney;
	}
	public int getAverageDistanceStart() {
		return averageDistanceStart;
	}
	public void setAverageDistanceStart(int averageDistanceStart) {
		this.averageDistanceStart = averageDistanceStart;
	}
	public int getAverageDistanceEnding() {
		return averageDistanceEnding;
	}
	public void setAverageDistanceEnding(int averageDistanceEnding) {
		this.averageDistanceEnding = averageDistanceEnding;
	}
	
	
	

}
